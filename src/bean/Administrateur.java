package bean;

public class Administrateur{
	private Long id_admin;
    private String nomA;
	private String prenomA;
    private String emailA;
    private String passwordA;
    private String cinA;
    private String adresseA;
    private String num_telephoneA;
	public Long getId_admin() {
		return id_admin;
	}
	public void setId_admin(Long id_admin) {
		this.id_admin = id_admin;
	}
	public String getNomA() {
		return nomA;
	}
	public void setNomA(String nomA) {
		this.nomA = nomA;
	}
	public String getPrenomA() {
		return prenomA;
	}
	public void setPrenomA(String prenomA) {
		this.prenomA = prenomA;
	}
	public String getEmailA() {
		return emailA;
	}
	public void setEmailA(String emailA) {
		this.emailA = emailA;
	}
	public String getPasswordA() {
		return passwordA;
	}
	public void setPasswordA(String passwordA) {
		this.passwordA = passwordA;
	}
	public String getCinA() {
		return cinA;
	}
	public void setCinA(String cinA) {
		this.cinA = cinA;
	}
	public String getAdresseA() {
		return adresseA;
	}
	public void setAdresseA(String adresseA) {
		this.adresseA = adresseA;
	}
	public String getNum_telephoneA() {
		return num_telephoneA;
	}
	public void setNum_telephoneA(String num_telephoneA) {
		this.num_telephoneA = num_telephoneA;
	}
    
    
    
}
